class Experience {
    
    constructor(title, type, dateEnd, dateStart, skills, place, description){
        this.title = title;
        this.type = type;
        this.dateEnd = dateEnd;
        this.dateStart = dateStart;
        this.skills = skills;
        this.place = place;
        this.description = description;
    }

    
}

module.exports = Experience;